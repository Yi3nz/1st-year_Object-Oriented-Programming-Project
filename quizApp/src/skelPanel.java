    private void delQuesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delQuesActionPerformed
        // TODO add your handling code here:
        if(question.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Enter a question please!");
        }
        else if(count ==0){
            JOptionPane.showMessageDialog(null,"No items to be deleted!");
        }
        else{
            Question = question.getText();
            
            for(int i = 0; i < aList.size();i++){
                if(aList.get(i).getQuestion().equals(Question)){
                    aList.remove(i);
                    count = count - 1;
                    JOptionPane.showMessageDialog(null,"Question at index "+ i + " is deleted!");
                    break;
                }
                if(i==count-1){
                    JOptionPane.showMessageDialog(null,"No item found!");
                }
            }
        }
    }//GEN-LAST:event_delQuesActionPerformed

