    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        name = nameTF.getText();

        quizApp a = new quizApp();
        a.setName(name);

        aList.add(a);

        nameTF.setText("");

        LayoutManager layout = getParent().getLayout();
        if (layout instanceof CardLayout) {
            CardLayout cl = (CardLayout) layout;
            cl.show(getParent(), "maths");
        }
        JOptionPane.showMessageDialog(null, "Name saved to ArrayList(Scores are not yet saved or displayed on the Result screen)");
        JOptionPane.showMessageDialog(null, "Repeat quiz to check previous name");

    }//GEN-LAST:event_addBtnActionPerformed

